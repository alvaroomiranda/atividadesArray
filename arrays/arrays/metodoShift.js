// MÉTODO "SHIFT" SERVE PRA REMOVER O PRIMEIRO ELEMENTO NO ARRAY:

// EXEMPLO:

const fruits = ["Apple", "Orange", "Banana", "Pear"]

fruits.shift();

console.log(fruits);

// Nesse exemplo, ele remove a maçã,"Apple", que é o primeiro elemento dentro do array

// Se quiser remover mais de um elemento é so adicionar mais um "fruits.shift();"
